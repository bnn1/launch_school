/**
 * THE PROBLEM
 * =======================
 * Take two numbers and return the result of their multiplication
 * input: number, number
 * output: number - result of multiplication of input numbers
 *
 * EXAMPLES AND TEST CASES
 * =======================
 * multiply(5, 3); // 15
 * multiply(-2, 10); // -20
 *
 * DATA STRUCTURES
 * ======================
 * numbers
 *
 * ALGORITHM
 * ======================
 * return multiplication of input numbers
 */

// CODE IMPLEMENTATION

module.exports = function multiply(number1, number2) {
  return number1 * number2;
};
